package com.kh.class4;

public class Shape {
	
	private double width;
	private double height;	

	public Shape() {};
	
		
	public void setwidth(double w) {
		this.width = w;
	}
	
	public  void setheight(double h) {
		this.height = h;
	}
	public  double getWidth() {
		return width;
	}
	
	public double getHeight() {
		return height;
	}




}
